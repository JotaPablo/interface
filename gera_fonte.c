/*
 * Este código processa sprites gerados no Piskel (new_piskel) para convertê-los em
 * um formato de fonte compatível com sistemas embarcados. O objetivo é transformar
 * sprites 8x8 em dados binários que podem ser usados como fonte em displays ou interfaces gráficas.
 *
 * Funcionamento geral:
 * 1. Transforma os pixels coloridos do Piskel em uma matriz binária (0s e 1s)
 * 2. Converte a matriz binária em valores binários compactados por coluna
 * 3. Formata os dados para hexa para utilizar no arquivos de fonte
 * 4. Ao compilar, será gerado no terminal o código hexadecimal de cada letra para utilizar no arquivo de fonte
 */

#include <stdint.h>
#include <stdio.h>

// Configurações do sprite gerado pelo Piskel
#define NEW_PISKEL_FRAME_COUNT 32  // 26 letras + 6 caracteres especiais
#define NEW_PISKEL_FRAME_WIDTH 8
#define NEW_PISKEL_FRAME_HEIGHT 8  

// Dados originais do Piskel
static const uint32_t new_piskel_data[32][64];

// Buffer para armazenar a versão binária (0/1) dos pixels
uint8_t dados[32][64] = {0};

// Converte os dados coloridos do Piskel em uma matriz binária
void transforma_piskel_em_dados() {
    int num = 0;
    for(int i = 0; i < NEW_PISKEL_FRAME_COUNT; i++) {
        printf("{\n");

        // Processa cada pixel do frame (8x8 = 64 pixels)
        for(int j = 0; j < NEW_PISKEL_FRAME_HEIGHT * NEW_PISKEL_FRAME_WIDTH; j++) {
            // Simplificação: considera qualquer valor não zero como 1
            num = (new_piskel_data[i][j] != 0) ? 1 : 0;
            dados[i][j] = num;

            // Formatação visual para debug/verificação
            if(j != 0 && (j+1) % 8 == 0)
                printf("%d,\n", num);
            else
                printf("%d, ", num);
        }
        printf("},\n");
    }
}

// Buffer para armazenar as colunas compactadas em formato binário (16 bits)
uint16_t binario[NEW_PISKEL_FRAME_COUNT][NEW_PISKEL_FRAME_HEIGHT] = {0};

// Mapeamento de caracteres especiais (complemento aos 26 caracteres alfabéticos)
char carac_especial[6] = {'?', '!', '.', '(', ')', ':'};  // Índices 26-31

// Converte a matriz binária em representação compactada por colunas
void transforma_dados_em_binario() {
    for(int i = 0; i < NEW_PISKEL_FRAME_COUNT; i++) {
        // Processa as colunas de baixo para cima (compatível com formatos de fonte)
        for(int j = NEW_PISKEL_FRAME_HEIGHT * NEW_PISKEL_FRAME_WIDTH - 1; j >= 0; j--) {
            uint8_t coluna = j % NEW_PISKEL_FRAME_WIDTH;
            
            // Empacota 8 bits verticalmente em uma palavra de 16 bits
            binario[i][coluna] = (binario[i][coluna] << 1) | dados[i][j];
        }
    }
}

// Imprime os dados formatados como array de fonte pronto para uso
void printa_fonte() {
    for(int i = 0; i < NEW_PISKEL_FRAME_COUNT; i++) {
        // Imprime valores hexadecimais para cada linha da fonte
        for(int j = 0; j < NEW_PISKEL_FRAME_HEIGHT; j++) {
            printf("0x%02X, ", binario[i][j]);
        }

        // Adiciona comentário com caractere correspondente
        if(i < 26) {
            printf("// %c\n", 'a' + i);  // Letras minúsculas
        } else if(i - 26 < sizeof(carac_especial)) {
            printf("// %c\n", carac_especial[i - 26]);  // Caracteres especiais
        } else {
            printf("// caractere indefinido\n");
        }
    }
}

int main() {
    transforma_piskel_em_dados();   // Passo 1: Conversão para binário
    transforma_dados_em_binario();  // Passo 2: Compactação vertical
    printa_fonte();                 // Passo 3: Saída formatada
    
    return 0;
}

